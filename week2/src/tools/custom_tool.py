from langchain.tools import BaseTool
from typing import Type
from pydantic import BaseModel, Field
import logging

logger = logging.getLogger(__name__)


class CustomToolInput(BaseModel):
    """Схема входных параметров для распознавания графика."""
    query: str = Field(
        description="Описание графика или запрос на распознавание кривой"
    )


# ═════════════════════════════════════════════════════════════════════════════
# ШАГ 2: Реализуйте класс инструмента
# ═════════════════════════════════════════════════════════════════════════════


class CustomTool(BaseTool):
    """
    Инструмент распознавания кривых с технических графиков.

    Назначение:
        Автоматическое извлечение данных кривой с изображений
        технических графиков с помощью нейросетевых моделей (CV).

        Учебная версия возвращает mock-данные.
        В production: подключение CV-модели детекции кривой
        и веб-интерфейса для редактирования результата.

    Пример использования:
        tool = CustomTool()
        result = tool.run(query="Распознай кривую с графика")

    Интеграция с дипломом:
        Является основным модулем CV-распознавания в информационной
        системе. Результат (точки кривой) передаётся на веб-интерфейс
        для визуализации и ручного редактирования.
    """

    # Обязательные атрибуты
    name: str = "chart_curve_detector"
    description: str = """
    Инструмент для распознавания кривых с технических графиков.
    Используйте для извлечения данных с изображений графиков.
    Возвращает нормализованные координаты точек кривой.
    """
    args_schema: Type[BaseModel] = CustomToolInput

    def _run(self, query: str) -> str:
        """
        Заглушка детекции кривой.

        Args:
            query: Запрос на распознавание

        Returns:
            str: Mock-результат с точками кривой
        """
        logger.info(f"Запрос на распознавание: {query}")

        result = (
            "Результат распознавания графика (mock):\n"
            "Найдено точек: 50\n"
            "Кривая: синусоида\n"
            "Диапазон X: 0.0 - 1.0\n"
            "Диапазон Y: 0.0 - 1.0\n\n"
            "Первые 5 точек:\n"
            "  (0.00, 0.50)\n"
            "  (0.02, 0.60)\n"
            "  (0.04, 0.70)\n"
            "  (0.06, 0.79)\n"
            "  (0.08, 0.87)\n\n"
            "Все точки готовы для отображения на веб-интерфейсе."
        )

        return result

    async def _arun(self, query: str) -> str:
        """Асинхронная версия."""
        return self._run(query)

    def to_langchain_tool(self) -> BaseTool:
        """Конвертация в формат LangChain."""
        return self


